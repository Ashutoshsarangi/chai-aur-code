//   Activity 1: Variable Declaration 

    // Task 1: Declare a variable using var, assign it a number, and log the value to the console. 
    
    var varVariable = 123;
    console.log('varVariable ---> ', varVariable);

    // Task 2: Declare a variable using let, assign it a string, and log the value to the console. 
    var letVariable = 'hello world';
    console.log('varVariable ---> ', letVariable);
    

//   Activity 2: Constant Declaration 

    // Task 3: Declare a variable using const, assign it a boolean value, and log the value to the console
    
    var constVariable = false;
    console.log('varVariable ---> ', constVariable);
    

//   Activity 3: Data Types 

    

    // Task 4: Create variables of different data types (number, string, boolean, object, array) and log each variable's type using the typeof operator. 
    const numberType = 123;
    const stringType = '123';
    const booleanType = false;
    const objectType = {
        name: 'Ashutosh'
    }
    const arrayType = [1, 2, 3];
    
    console.log('type of number Variable --> ', typeof numberType)
    console.log('type of string Variable --> ', typeof stringType)
    console.log('type of boolean Variable --> ', typeof booleanType)
    console.log('type of object Variable --> ', typeof objectType)
    console.log('type of array Variable --> ', typeof arrayType)


//   Activity 4: Reassigning Variables 

    

    // Task 5: Declare a variable using let, assign it an initial value, reassign a new value, and log both values to the console. 
    let initialLetValue = 123;
    initialLetValue = '123';
    
    console.log('initialLetValue --> ', initialLetValue);
    

